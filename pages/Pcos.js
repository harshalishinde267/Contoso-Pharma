            <p>The diagnosis of PCOS is based on Hyperandrogenism and chronic anovulation in the absence of specific pituitary or adrenal disease , and have disrupted ovulatory function with chronic oligomenorrhea (cycle length greater than 35 day) or amenorrhea (cycle length greater than 12 week) and typical appearance of polycystic ovaries by ultrasound according to the criteria of the Rotterdam consensus meeting 2003 for diagnosis of PCOS. The different diagnostic tests needed to adequately assess for the possibility of PCOS e.g. Pregnancy test, TSH level (for Hyperthyroidism), Prolactin test (for Hyperprolactinemia), Total testosterone (for ovarian tumor) and some tests forevaluating the insulin resistance syndrome in women: Waist circumference (greater than 88 cm), Triglycerides (greater than 150 mg/dL), HDL Cholesterol (less than 50 mg/dL), Blood pressure (greater than 130/85) and Fasting glucose (greater than 110 mg/dL). Fasting glucose- to- insulin ratio and 2 hour oral glucose tolerance test (2h- OGTT 140 - 199 mg/dL) may be better predictor of insulin resistance .</p>
